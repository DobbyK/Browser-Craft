function update (){

	if (cursors.left.isDown){
    	player.setVelocityX(-160);
	} else if (cursors.right.isDown) {
    	player.setVelocityX(160);
	} else if (cursors.up.isDown && player.body.touching.down) {
    	player.setVelocityY(-330);	
	} else {
   		player.setVelocityX(0);
	} 
}
